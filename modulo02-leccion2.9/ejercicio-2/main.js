'use strict';

let oneHundred = [];

function get100Numbers() {

    for (let i = 1; i <= 100; i++) {
        console.log(oneHundred.push(i))
    }
    console.log(oneHundred);
}
get100Numbers();
console.log(oneHundred);

function getReversed100Numbers() {
    console.log(oneHundred.reverse());

}
getReversed100Numbers();













